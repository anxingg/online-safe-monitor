package cn.com.qytx.cbb.notify.domain;

import java.util.Date;

/**
 * 公告的统计结果类
 * @author lilipo
 *
 */
public class NotifyStatisticsResult {
	
	/**
	 * 发布时间开始
	 */
	private Date beginDate;
	
	/**
	 * 发布时间结束
	 */
	private Date endDate;
	
	/**
	 * 发布时间开始（字符串，接收参数用）
	 */
	private String beginDateStr;
	
	/**
	 * 发布时间结束（字符串，接收参数用）
	 */
	private String endDateStr;
	
	/**
	 * 公告类型id
	 */
	private Integer notifyType;
	
	/**
	 * 公告类型名称
	 */
	private String notifyTypeName;

	/**
	 * 发布次数
	 */
	private Long count;
	
	/**
	 * 栏目类型
	 */
	private Integer columnId;
	
	
	public NotifyStatisticsResult() {
		super();
	}
	
	public NotifyStatisticsResult(Integer notifyType, Long count) {
		super();
		this.notifyType = notifyType;
		this.count = count;
	}

	public NotifyStatisticsResult(Integer notifyType, Long count,
			Integer columnId) {
		super();
		this.notifyType = notifyType;
		this.count = count;
		this.columnId = columnId;
	}

	public Date getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(Date beginDate) {
		this.beginDate = beginDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public Integer getNotifyType() {
		return notifyType;
	}

	public void setNotifyType(Integer notifyType) {
		this.notifyType = notifyType;
	}

	public String getNotifyTypeName() {
		return notifyTypeName;
	}

	public void setNotifyTypeName(String notifyTypeName) {
		this.notifyTypeName = notifyTypeName;
	}

	public Long getCount() {
		return count;
	}

	public void setCount(Long count) {
		this.count = count;
	}

	public Integer getColumnId() {
		return columnId;
	}

	public void setColumnId(Integer columnId) {
		this.columnId = columnId;
	}

	public String getBeginDateStr() {
		return beginDateStr;
	}

	public void setBeginDateStr(String beginDateStr) {
		this.beginDateStr = beginDateStr;
	}

	public String getEndDateStr() {
		return endDateStr;
	}

	public void setEndDateStr(String endDateStr) {
		this.endDateStr = endDateStr;
	}
	
}
