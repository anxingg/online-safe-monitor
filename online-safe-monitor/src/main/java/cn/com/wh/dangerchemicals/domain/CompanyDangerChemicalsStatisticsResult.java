package cn.com.wh.dangerchemicals.domain;


/**
 * 公司危险化学品统计结果
 * @author lilipo
 *
 */
public class CompanyDangerChemicalsStatisticsResult {


	private Integer groupId;//企业ID
	
	private String groupName;//企业名称
	
	private Integer dangerId;//危险化学品ID
	
	private String materialName;//物质名称（危险化学品名）
	
	private Float count;//数量
	
	public CompanyDangerChemicalsStatisticsResult(Integer dangerId, Float count) {
		super();
		this.dangerId = dangerId;
		this.count = count;
	}

	public CompanyDangerChemicalsStatisticsResult(Integer groupId,
			Integer dangerId, Float count) {
		super();
		this.groupId = groupId;
		this.dangerId = dangerId;
		this.count = count;
	}

	public CompanyDangerChemicalsStatisticsResult() {
		super();
	}

	public Integer getGroupId() {
		return groupId;
	}

	public void setGroupId(Integer groupId) {
		this.groupId = groupId;
	}

	public String getGroupName() {
		return groupName;
	}

	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	public Integer getDangerId() {
		return dangerId;
	}

	public void setDangerId(Integer dangerId) {
		this.dangerId = dangerId;
	}

	public String getMaterialName() {
		return materialName;
	}

	public void setMaterialName(String materialName) {
		this.materialName = materialName;
	}

	public Float getCount() {
		return count;
	}

	public void setCount(Float count) {
		this.count = count;
	}
	
}
