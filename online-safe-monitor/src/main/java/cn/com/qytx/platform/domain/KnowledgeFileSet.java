package cn.com.qytx.platform.domain;

import java.io.Serializable;
/**
 * 知识库luncene文件配置
 * @author 马恺
 */
public class KnowledgeFileSet implements Serializable{

	private static final long serialVersionUID = 1L;
	
	  //luncene根目录
	  public  String lunceneRoot;
	  //luncene初始化目录
	  public String lunceneInitFile;
	  //知识库上传路径
	  public String knowledgeUpload;
	  //参数描述
	  public String parDescribe;
	  //参数项
	  public String parItems;
	public String getLunceneRoot() {
		return lunceneRoot;
	}
	public void setLunceneRoot(String lunceneRoot) {
		this.lunceneRoot = lunceneRoot;
	}
	public String getLunceneInitFile() {
		return lunceneInitFile;
	}
	public void setLunceneInitFile(String lunceneInitFile) {
		this.lunceneInitFile = lunceneInitFile;
	}
	public String getKnowledgeUpload() {
		return knowledgeUpload;
	}
	public void setKnowledgeUpload(String knowledgeUpload) {
		this.knowledgeUpload = knowledgeUpload;
	}
	public String getParDescribe() {
		return parDescribe;
	}
	public void setParDescribe(String parDescribe) {
		this.parDescribe = parDescribe;
	}
	public String getParItems() {
		return parItems;
	}
	public void setParItems(String parItems) {
		this.parItems = parItems;
	}
	  
	  

}
