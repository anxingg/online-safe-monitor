package cn.com.qytx.platform.domain;

import java.io.Serializable;

public class IvrAndACDSet implements Serializable {

	/**
	 * 描述含义
	 */
	private static final long serialVersionUID = 1L;
	/**
	 * 封装ivr acd功能设置的相关信息
	 * 
	 * 2014-04-07 蔡明雪
	 */
	//是否开启坐席录音 0：不开启，1：开启
	private Integer isSeatRecord;
	//是否开启  自定义语音流程  0：不开启，1：开启
	private Integer isCustomVoice;
	//是否开启 外呼异地手机时加拨零  0：不开启 1：开启
	private Integer remotePhoneZero;
	//是否开启 留言功能  0：不开启 1：开启
	private Integer isLeaveMessage;
	//是否开启 满意度调查 0：不开启 1：开启
	private Integer isSatifyCheck;
	//是否开启 多接入号 0：不开启 1：开启
	private Integer isMultiAccessNum;
	//是否开启 黑名单功能 0：不开启 1:开启
	private Integer isBlack;
	
	//参数描述
	private String parDescribe;
	//参数项
	private String parItems;


	
	//默认的构造函数
	public IvrAndACDSet() {
		
	}
	
	public Integer getIsSeatRecord() {
		return isSeatRecord;
	}
	public void setIsSeatRecord(Integer isSeatRecord) {
		this.isSeatRecord = isSeatRecord;
	}
	public Integer getIsCustomVoice() {
		return isCustomVoice;
	}
	public void setIsCustomVoice(Integer isCustomVoice) {
		this.isCustomVoice = isCustomVoice;
	}
	public Integer getRemotePhoneZero() {
		return remotePhoneZero;
	}
	public void setRemotePhoneZero(Integer remotePhoneZero) {
		this.remotePhoneZero = remotePhoneZero;
	}
	public Integer getIsLeaveMessage() {
		return isLeaveMessage;
	}
	public void setIsLeaveMessage(Integer isLeaveMessage) {
		this.isLeaveMessage = isLeaveMessage;
	}
	public Integer getIsSatifyCheck() {
		return isSatifyCheck;
	}
	public void setIsSatifyCheck(Integer isSatifyCheck) {
		this.isSatifyCheck = isSatifyCheck;
	}
	public Integer getIsMultiAccessNum() {
		return isMultiAccessNum;
	}
	public void setIsMultiAccessNum(Integer isMultiAccessNum) {
		this.isMultiAccessNum = isMultiAccessNum;
	}
	public Integer getIsBlack() {
		return isBlack;
	}
	public void setIsBlack(Integer isBlack) {
		this.isBlack = isBlack;
	}
	public String getParDescribe() {
		return parDescribe;
	}
	public void setParDescribe(String parDescribe) {
		this.parDescribe = parDescribe;
	}
	public String getParItems() {
		return parItems;
	}
	public void setParItems(String parItems) {
		this.parItems = parItems;
	}
	
	
	
}
